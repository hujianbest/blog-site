const { chromium } = require('C:/tools/browser-test/node_modules/playwright');
(async () => {
  const issues = [];
  const requests = [];
  const browser = await chromium.launch({ headless: true, executablePath: 'C:/Program Files/Google/Chrome/Application/chrome.exe' });
  const page = await browser.newPage({ viewport: { width: 1366, height: 768 } });
  page.on('console', (m) => { if (m.type() === 'error') issues.push({ type: 'console', text: m.text() }); });
  page.on('pageerror', (e) => issues.push({ type: 'pageerror', text: e.message }));
  page.on('request', (req) => { if (req.url().includes('/api/')) requests.push({ method: req.method(), url: req.url(), postData: req.postData() }); });
  const baseUrl = process.env.SMOKE_BASE_URL || 'http://127.0.0.1:5177';
  await page.goto(`${baseUrl}/#/`, { waitUntil: 'networkidle' });
  await page.evaluate(() => localStorage.clear());
  const writeSelector = 'header a[href="/write"], header a[href="#/write"]';
  const writeLink = await page.locator(writeSelector).count();
  await page.click(writeSelector);
  await page.waitForURL('**/#/write');
  await page.waitForLoadState('networkidle');
  const title = ((await page.locator('h1').textContent()) || '').trim();
  await page.fill('input[aria-label="文章标题"]', '浏览器验证草稿');
  await page.fill('textarea[data-test="markdown-editor"], textarea', '# 浏览器验证草稿\n\n这是一段从浏览器写入的正文。');
  await page.click('[data-action="save-draft"]');
  await page.waitForTimeout(1200);
  const statusText = await page.locator('[data-ui-state="success"], [data-ui-state="error"]').textContent().catch(() => '');
  const localArticles = await page.evaluate(() => JSON.parse(localStorage.getItem('blog-site-local-articles') || '[]'));
  const report = { writeLink, title, statusText: (statusText || '').trim(), localArticlesCount: localArticles.length, latestLocalTitle: localArticles[0]?.title, requests, issues };
  console.log(JSON.stringify(report, null, 2));
  await browser.close();
  if (!writeLink || !title.includes('开始写作') || !statusText.includes('草稿已保存') || localArticles[0]?.title !== '浏览器验证草稿' || requests.some((r) => r.method !== 'GET') || issues.length) process.exit(1);
})();
